package Lab1;

public class Calculate {

    int a;
    int b;

    public Calculate (int a, int b) {
        this.a = a;
        this.b = b;
    }

   
    public int multiply() {
        return a * b;
    }

    public int division() {
        return a / b;
    }
    
     public int minus() {
        return a - b;
    }
     
    public int plus() {
        return a + b;
    }

   
}
